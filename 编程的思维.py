i = 1  # 初始化 计数器从1开始。记录循环次数，从第一次循环开始。
sum = 0  # 初始化 累加器 从0开始
for i in range(1, 101):
    sum = sum + i # 累加器，将每次的循环结果累加起来。
print(sum)
#开始-->计数器初始化从1开始-->累加器初始化从0开始-->从1——100遍历（循环）-->累加每次结果-->输出结果-->结束


i = 1
sum1 = 0
while i <= 100:
    sum1 = sum1 + i
    i = i + 1
print(sum1)
#开始-->计数器初始化-->累加器初始化-->如i<=100-->累加-->循环次数(计数器）更新---->输出结果-->结束

"""小白：
开始
1. 初始化计数器: i = 1
2. 初始化累加器: sum1 = 0
3. 循环条件检查: 如 i <= 100 成立，则进入步骤4；否则跳至步骤6
4. 循环体:
   a. 累加: sum1 = sum1 + i
   b. 计数器更新: i = i + 1
   c. 返回步骤3
5. 循环结束
6. 输出结果: print(sum1)
7. 结束
"""

def temperature(c):
    f=c*1.8+32
    return f
c=float(input('输入当前摄氏度：'))
f=temperature(c)
print(f)

# 开始-->执行定义函数-->函数内部华氏度的计算方法-->执行返回-->输入调用函数-->执行得到华氏度-->输出-->结束
#开始 ---->输入摄氏度---->调用函数---->输出华氏度---->结束

