"""
数据类型一般分为：整数 integers,用int代表.
浮点数floating point number,用float代表.
复数：complex numbers ,包含一个实部和虚部
"""
print(4324342 / 432)  # 除法运算。得到是浮点数
print(43223423 % 2)  # 取模计算（求余数）。取模计算不是//吗？求商，舍弃小数部分，并且向下取整。

print(4432423 // 43)  # ---->103079 求商，舍弃了小数部分

# 正整数整除（直接舍弃小数，和日常认知一致）
print(10 // 3)  # 10 ÷ 3 ≈ 3.333，舍弃小数，输出：3
print(7 // 2)   # 7 ÷ 2 = 3.5，舍弃小数，输出：3

# 包含负整数的整除（重点：向下取整，不是简单舍弃小数）
print(10 // -3) # 10 ÷ (-3) ≈ -3.333，向下取整为 -4，输出：-4
print(-10 // 3) # -10 ÷ 3 ≈ -3.333，向下取整为 -4，输出：-4


