print(0.1 + 0.2 == 0.3)  # ---->False

import decimal  # 引入 decimal模块 十进制的意思

e = decimal.Decimal(0.1)  # 括号内是数字,浮点数，这时候还是不精确的小数，结果也是小数浮点数。
f = decimal.Decimal(0.2)
print(e + f)  # ---->0.3000000000000000166533453694

e = decimal.Decimal('0.1')  # 括号内是字符串，decimal会直接用十进制来计算，结果就是精确的0.3
f = decimal.Decimal('0.2')
print(e + f)  # ---->0.3

# E记法 科学计数法

l = 0.00005
print(l)  # ----->5e-05 5的10的负5次方。

# 复数： complex numbers
x = 1 + 2j  # 1是实部，2j是虚部,都是浮点数来存放
print(x.real)  # ---->获得实部1.0(浮点数）
print(x.imag)  # ---->获得虚部2.0（浮点数）
print(x)
